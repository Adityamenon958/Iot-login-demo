async function renderPdfReport(payload) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: MARGIN, bufferPages: true });
    const chunks = [];
    doc.on('data', (c) => chunks.push(c));
    doc.on('end', () => resolve(Buffer.concat(chunks)));
    doc.on('error', reject);

    const debugState = { enabled: isLayoutDebugEnabled(), events: [] };

    drawPageTemplate(doc);
    renderCoverPage(doc, payload);
    const sections = buildSectionDescriptors(payload);
    const layoutContext = {
      margin: MARGIN,
      contentWidth: CONTENT_WIDTH,
      sectionGap: SECTION_GAP,
    };
    const { pages } = buildLayoutPlan({
      sections,
      context: layoutContext,
      pageTop: PAGE_TOP,
      pageBottom: PAGE_BOTTOM,
      firstTemplate: 'summary',
      debugState,
    });

    const renderablePages = pages.filter((page) => page.frames?.length);
    renderablePages.forEach((page, pageIdx) => {
      addReportPage(doc);
      let flowY = PAGE_TOP;
      page.frames.forEach((frame, frameIndex) => {
        const section = sections.find((s) => s.id === frame.sectionId);
        if (!section) return;
        if (flowY > PAGE_BOTTOM - 48) {
          addReportPage(doc);
          flowY = PAGE_TOP;
        }
        const flowFrame = { ...frame, y: flowY };
        let endY;
        LAYOUT_MANAGED_RENDER = true;
        try {
          endY = section.render(doc, layoutContext, flowFrame);
        } finally {
          LAYOUT_MANAGED_RENDER = false;
        }
        const actualEnd = Number.isFinite(endY) ? endY : flowFrame.y + frame.height;
        if (actualEnd > PAGE_BOTTOM) {
          flowY = PAGE_TOP;
          if (frameIndex < page.frames.length - 1) {
            addReportPage(doc);
          }
        } else {
          flowY = actualEnd + SECTION_GAP;
        }
        if (debugState.enabled) {
          drawDebugOverlay(doc, flowFrame, PAGE_BOTTOM - flowY);
        }
      });
      if (debugState.enabled) {
        doc.fontSize(6.5).fillColor('#6c757d').text(
          `Template: ${page.template} · pageIndex=${pageIdx} · debug=true`,
          MARGIN,
          PAGE_TOP - 14
        );
      }
    });

    // Footer stamping is temporarily disabled because PDFKit can auto-paginate
    // if footer text is written outside the writable bottom margin, which can
    // create cascading blank pages.

    doc.end();
  });
}

