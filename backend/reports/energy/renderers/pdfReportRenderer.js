function scaleForVariant(height, variant) {
  if (variant === SECTION_VARIANTS.MEDIUM) return Math.max(40, Math.floor(height * 0.88));
  if (variant === SECTION_VARIANTS.COMPACT) return Math.max(32, Math.floor(height * 0.78));
  return height;
}

function fixedVariants(height) {
  return {
    [SECTION_VARIANTS.LARGE]: height,
    [SECTION_VARIANTS.MEDIUM]: height,
    [SECTION_VARIANTS.COMPACT]: height,
  };
}

